int		ft_count_if(char **tab, int (*f)(char*))
{
	int	flag;
	int	i;

	i = 1;
	flag = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			flag++;
		i++;
	}
	return (flag);
}
